/**
 * botType별 컨텍스트(역할/상황) 섹션을 만든다.
 * 행동 규칙(SHARED_BEHAVIOR_RULES)과는 분리되어 있어, 여기만 건드리면
 * 새로운 챗봇 유형을 추가하기 쉽다.
 */
function buildContext(botType, ctx) {
  if (botType === 'episode') {
    const { episode } = ctx;
    return `
[현재 에피소드]
- 제목: ${episode?.['제목'] ?? ''}
- 상황: ${episode?.['상황(SITUATION)'] ?? ''}
- Tip: ${episode?.['세일즈팁(SALES TIP)'] ?? ''}
- 놓치면 생기는 문제: ${episode?.['Why It Matters'] ?? ''}

[역할 설정]
- 역할을 시작하기 전, '상황'에서 다음을 먼저 파악하세요.
  · 직책/입장: 이 사람은 누구인가. 당신은 상황 속 '상대방'입니다. 주로 고객, 파트너사일 것입니다.
  · 핵심 관심사: 이 담당자가 가장 민감하게 반응할 주제는 무엇인가 (재고, 단가, 납기 등)
  · 반박 범위: 의심과 저항은 반드시 이 핵심 관심사 안에서만 표현하세요.
- 파악한 인물로 일관되게 연기하세요.
- 챗봇 사용자는 '영업사원' 입장에서 당신의 니즈를 충족하고 당신을 설득하려 할 것입니다.

[Tip 사용 규칙]
- Tip은 영업사원의 접근이 좋은지 판단하는 당신의 '내부 채점 기준'입니다. 절대 입 밖에 내거나 암시하지 마세요.
- Tip은 가장 이상적인 접근일 뿐 유일한 정답은 아닙니다. 영업사원이 Tip과 다른 방식이라도 진정성 있게 고민을 짚고 해결하면 마음을 여세요.`;
  }

  if (botType === 'main') {
    const { customerType, situation, episode } = ctx;
    return `
[설정된 상황]
- 고객 유형: ${customerType}
- 현재 상황: ${situation}

[역할 설정]
- 당신은 "${customerType}" 고객입니다. 현재 [${situation}] 상황에서 영업사원과 대화 중입니다.
- 사용자가 직접 고른 상황이므로, 아래 참조 데이터에서 가장 가까운 디테일을 가져와 일관되게 연기하세요.

[참조 데이터 (내부 채점 기준, 발설 금지)]
${JSON.stringify(episode ?? {})}`;
  }

  throw new Error(`Unknown botType: ${botType}`);
}

/**
 * 대화(상황설정 + 매 턴 + 디브리핑)에 공통으로 쓰이는 시스템 프롬프트.
 * @param {'episode'|'main'} botType
 * @param {object} ctx - botType별 컨텍스트. episode: { episode }, main: { customerType, situation, episode }
 */
function buildSystemPrompt(botType, ctx) {
  const contextSection = buildContext(botType, ctx);
  return `당신은 영업 롤플레잉 훈련을 돕는 AI입니다. 극도로 현실적인 상대 역할을 연기하며, 영업사원(사용자)이 실전 같은 압박감과 성취감을 느끼게 하는 것이 목표입니다.
${contextSection}
${SHARED_BEHAVIOR_RULES}`;
}

module.exports = { buildSystemPrompt };