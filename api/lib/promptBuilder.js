// 두 챗봇(episode / main)이 공유하는 시스템 프롬프트 빌더.
//
// 설계 원칙:
// - "역할에 몰입해서 대화하고, 매 턴 상태를 마커로 보고하고, 끝나면 JSON 리포트를 낸다"는
//   행동 규칙(rules)은 botType과 무관하게 100% 동일하다.
// - botType별로 다른 건 오직 "이 역할이 누구이고 어떤 상황인지"를 채워주는 컨텍스트뿐이다.
// - 따라서 buildContext(botType, ctx)만 분기하고, 행동 규칙은 단일 정의(BEHAVIOR_RULES)를 공유한다.
//
// 종료 프로토콜 (두 챗봇 공통, SCORE 기반 객관적 종료 규칙):
// - 매 턴 응답 마지막 줄에 반드시 [SCORE: 0~100] 포함 (두 챗봇 모두 예외 없이 필수)
// - 종료 여부는 모델의 자의적 판단이 아니라 서버(api/turn.js)가 점수만 보고 결정한다:
//   score <= 0 → fail로 강제 종료, score >= 100 → success로 강제 종료
// - 사용자가 "종료" 버튼으로 조기 종료를 요청하면(트리거 메시지) 점수를 0 또는 100 중
//   하나로 명확히 맞추도록 모델에 지시 → 서버가 그 점수를 보고 동일한 규칙으로 종료 처리
// - 디브리핑(평가 리포트) 요청 시 [SCORE] 마커 없이 순수 JSON만 출력

